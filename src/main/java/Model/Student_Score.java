package Model;

public class Student_Score {
    private String name;
    private String term_text;
    private Integer term_no;
    private String score_name;
    private Integer score_point;
    private String score_type;
    private String score_score;
    private String score_number;
    private String score_remark;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTerm_text() {
        return term_text;
    }

    public void setTerm_text(String term_text) {
        this.term_text = term_text;
    }

    public Integer getTerm_no() {
        return term_no;
    }

    public void setTerm_no(Integer term_no) {
        this.term_no = term_no;
    }

    public String getScore_name() {
        return score_name;
    }

    public void setScore_name(String score_name) {
        this.score_name = score_name;
    }

    public Integer getScore_point() {
        return score_point;
    }

    public void setScore_point(Integer score_point) {
        this.score_point = score_point;
    }

    public String getScore_type() {
        return score_type;
    }

    public void setScore_type(String score_type) {
        this.score_type = score_type;
    }

    public String getScore_score() {
        return score_score;
    }

    public void setScore_score(String score_score) {
        this.score_score = score_score;
    }

    public String getScore_number() {
        return score_number;
    }

    public void setScore_number(String score_number) {
        this.score_number = score_number;
    }

    public String getScore_remark() {
        return score_remark;
    }

    public void setScore_remark(String score_remark) {
        this.score_remark = score_remark;
    }
}
